import UIKit

extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4537427381, green: 0.4332568764, blue: 0.715835184, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.1248660109, green: 0.1542348359, blue: 0.4331376904, alpha: 0.3480843322)
    }
}
